DROP TABLE IF EXISTS DietPlanMeal;
DROP TABLE IF EXISTS DietPlan;
DROP TABLE IF EXISTS GymClassBooking;
DROP TABLE IF EXISTS GymClass;
DROP TABLE IF EXISTS TrainerBooking;
DROP TABLE IF EXISTS WorkoutSessionSet;
DROP TABLE IF EXISTS WorkoutSession;
DROP TABLE IF EXISTS AlternativeMeal;
DROP TABLE IF EXISTS WorkoutExercise;
DROP TABLE IF EXISTS ProgressEntry;
DROP TABLE IF EXISTS MealLog;
DROP TABLE IF EXISTS Subscription;
DROP TABLE IF EXISTS WorkoutPlan;
DROP TABLE IF EXISTS User;
DROP TABLE IF EXISTS Exercise;
DROP TABLE IF EXISTS Trainer;

CREATE TABLE IF NOT EXISTS User (
  id INT AUTO_INCREMENT PRIMARY KEY,
  email VARCHAR(255) NOT NULL UNIQUE,
  password VARCHAR(255) NOT NULL,
  name VARCHAR(255) NOT NULL,
  username VARCHAR(255) UNIQUE DEFAULT NULL,
  avatar LONGTEXT DEFAULT NULL,
  role VARCHAR(50) NOT NULL DEFAULT 'USER',
  subscriptionPlan VARCHAR(255) NOT NULL DEFAULT 'free',
  theme VARCHAR(20) NOT NULL DEFAULT 'dark',
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS Exercise (
  id INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  category VARCHAR(255) NOT NULL,
  difficulty VARCHAR(255) NOT NULL,
  description TEXT NOT NULL,
  instructions TEXT NOT NULL,
  muscleGroups VARCHAR(255) NOT NULL,
  imageUrl VARCHAR(255) DEFAULT NULL,
  videoUrl VARCHAR(255) DEFAULT NULL,
  equipment VARCHAR(255) DEFAULT 'bodyweight',
  defaultSets INT DEFAULT 3,
  defaultReps INT DEFAULT 10,
  defaultTimeSecs INT DEFAULT NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS Trainer (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT UNIQUE DEFAULT NULL,
  name VARCHAR(255) NOT NULL,
  title VARCHAR(255) NOT NULL DEFAULT '',
  specialty VARCHAR(255) NOT NULL,
  specialtyIcon VARCHAR(100) NOT NULL DEFAULT 'faDumbbell',
  specialtyColor VARCHAR(30) NOT NULL DEFAULT 'var(--accent-cyan)',
  avatar VARCHAR(10) NOT NULL DEFAULT '🏋️',
  bio TEXT NOT NULL,
  certifications TEXT NOT NULL,
  tags TEXT NOT NULL,
  imageUrl VARCHAR(255) DEFAULT NULL,
  rating FLOAT NOT NULL DEFAULT 0,
  reviews INT NOT NULL DEFAULT 0,
  experience INT NOT NULL DEFAULT 0,
  pricePerSession FLOAT NOT NULL DEFAULT 50,
  sessionsCompleted INT NOT NULL DEFAULT 0,
  available BOOLEAN NOT NULL DEFAULT TRUE,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS WorkoutPlan (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT NOT NULL,
  name VARCHAR(255) NOT NULL,
  description TEXT DEFAULT NULL,
  daysPerWeek INT NOT NULL DEFAULT 3,
  isActive BOOLEAN NOT NULL DEFAULT FALSE,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS WorkoutExercise (
  id INT AUTO_INCREMENT PRIMARY KEY,
  planId INT NOT NULL,
  exerciseId INT NOT NULL,
  sets INT NOT NULL DEFAULT 3,
  reps INT NOT NULL DEFAULT 10,
  restSecs INT NOT NULL DEFAULT 60,
  day INT NOT NULL DEFAULT 1,
  orderIndex INT NOT NULL DEFAULT 0,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (planId) REFERENCES WorkoutPlan(id) ON DELETE CASCADE,
  FOREIGN KEY (exerciseId) REFERENCES Exercise(id)
);

CREATE TABLE IF NOT EXISTS ProgressEntry (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT NOT NULL,
  weight FLOAT DEFAULT NULL,
  bodyFat FLOAT DEFAULT NULL,
  notes TEXT DEFAULT NULL,
  date DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS MealLog (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT NOT NULL,
  name VARCHAR(255) NOT NULL,
  mealType VARCHAR(255) NOT NULL,
  calories INT NOT NULL,
  protein FLOAT NOT NULL DEFAULT 0,
  carbs FLOAT NOT NULL DEFAULT 0,
  fat FLOAT NOT NULL DEFAULT 0,
  date DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS Subscription (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT NOT NULL UNIQUE,
  plan VARCHAR(255) NOT NULL DEFAULT 'free',
  status VARCHAR(255) NOT NULL DEFAULT 'active',
  billingCycle VARCHAR(50) NOT NULL DEFAULT 'monthly',
  autoRenew BOOLEAN NOT NULL DEFAULT TRUE,
  maxVisits INT NOT NULL DEFAULT 0,
  usedVisits INT NOT NULL DEFAULT 0,
  startsAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  expiresAt DATETIME DEFAULT NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS GymClass (
  id INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  trainerId INT DEFAULT NULL,
  scheduledAt DATETIME NOT NULL,
  durationMins INT NOT NULL DEFAULT 60,
  maxSpots INT NOT NULL DEFAULT 20,
  spotsBooked INT NOT NULL DEFAULT 0,
  color VARCHAR(30) NOT NULL DEFAULT 'var(--accent-cyan)',
  description TEXT DEFAULT NULL,
  requiredPlan VARCHAR(50) NOT NULL DEFAULT 'basic',
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (trainerId) REFERENCES Trainer(id) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS GymClassBooking (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT NOT NULL,
  classId INT NOT NULL,
  status VARCHAR(50) NOT NULL DEFAULT 'confirmed',
  bookedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UNIQUE KEY unique_user_class (userId, classId),
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE CASCADE,
  FOREIGN KEY (classId) REFERENCES GymClass(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS TrainerBooking (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT NOT NULL,
  trainerId INT NOT NULL,
  scheduledAt DATETIME NOT NULL,
  durationMins INT NOT NULL DEFAULT 60,
  status VARCHAR(50) NOT NULL DEFAULT 'pending',
  notes TEXT DEFAULT NULL,
  totalPrice FLOAT NOT NULL DEFAULT 0,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE CASCADE,
  FOREIGN KEY (trainerId) REFERENCES Trainer(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS AlternativeMeal (
  id INT AUTO_INCREMENT PRIMARY KEY,
  injury VARCHAR(255) NOT NULL,
  icon VARCHAR(50) NOT NULL,
  name VARCHAR(255) NOT NULL,
  benefit TEXT NOT NULL,
  calories INT NOT NULL,
  protein FLOAT NOT NULL DEFAULT 0,
  carbs FLOAT NOT NULL DEFAULT 0,
  fat FLOAT NOT NULL DEFAULT 0,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS WorkoutSession (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT NOT NULL,
  planId INT DEFAULT NULL,
  name VARCHAR(255) NOT NULL,
  durationSecs INT NOT NULL DEFAULT 0,
  caloriesBurned INT NOT NULL DEFAULT 0,
  date DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE CASCADE,
  FOREIGN KEY (planId) REFERENCES WorkoutPlan(id) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS WorkoutSessionSet (
  id INT AUTO_INCREMENT PRIMARY KEY,
  sessionId INT NOT NULL,
  exerciseId INT NOT NULL,
  setNumber INT NOT NULL,
  reps INT DEFAULT NULL,
  weight FLOAT DEFAULT NULL,
  timeSecs INT DEFAULT NULL,
  isCompleted BOOLEAN NOT NULL DEFAULT TRUE,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (sessionId) REFERENCES WorkoutSession(id) ON DELETE CASCADE,
  FOREIGN KEY (exerciseId) REFERENCES Exercise(id)
);

CREATE TABLE IF NOT EXISTS DietPlan (
  id INT AUTO_INCREMENT PRIMARY KEY,
  planId VARCHAR(50) NOT NULL UNIQUE,
  label VARCHAR(100) NOT NULL,
  labelColor VARCHAR(50) NOT NULL,
  name VARCHAR(255) NOT NULL,
  goal VARCHAR(255) NOT NULL,
  goalIcon VARCHAR(100) NOT NULL,
  description TEXT NOT NULL,
  calories INT NOT NULL,
  protein FLOAT NOT NULL,
  carbs FLOAT NOT NULL,
  fat FLOAT NOT NULL,
  accentColor VARCHAR(50) NOT NULL,
  gradientFrom VARCHAR(100) NOT NULL,
  gradientTo VARCHAR(100) NOT NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS DietPlanMeal (
  id INT AUTO_INCREMENT PRIMARY KEY,
  dietPlanId INT NOT NULL,
  time VARCHAR(50) NOT NULL,
  name VARCHAR(255) NOT NULL,
  foods TEXT NOT NULL,
  calories INT NOT NULL,
  protein FLOAT NOT NULL,
  carbs FLOAT NOT NULL,
  fat FLOAT NOT NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (dietPlanId) REFERENCES DietPlan(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS SubscriptionBenefit (
  id INT AUTO_INCREMENT PRIMARY KEY,
  planId VARCHAR(50) NOT NULL,
  benefitText VARCHAR(255) NOT NULL,
  isIncluded BOOLEAN NOT NULL DEFAULT TRUE,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS UserBenefit (
  id INT AUTO_INCREMENT PRIMARY KEY,
  userId INT NOT NULL,
  benefitId INT NOT NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  usedCount INT NOT NULL DEFAULT 0,
  maxCount INT NOT NULL DEFAULT 0,
  expiresAt DATETIME DEFAULT NULL,
  FOREIGN KEY (userId) REFERENCES User(id) ON DELETE CASCADE,
  FOREIGN KEY (benefitId) REFERENCES SubscriptionBenefit(id) ON DELETE CASCADE
);

